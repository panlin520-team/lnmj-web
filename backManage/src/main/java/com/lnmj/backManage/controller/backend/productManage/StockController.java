package com.lnmj.backManage.controller.backend.productManage;

import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;

/**
 * @Author: yilihua
 * @Date: 2019/7/3 16:41
 * @Description: 仓库管理
 */
@Controller
@RequestMapping("/stock")
public class StockController {

    @RequestMapping("/toinstoragemanage")
    public String toinstoragemanage() {
        return "product/stock/InStorage";
    }

    @RequestMapping("/tooutstoragemanage")
    public String tooutstoragemanage() {
        return "product/stock/OutStorage";
    }

    @RequestMapping("/tostockproductmanage")
    public String toStockProductmanage() {
        return "product/stock/StockProduct";
    }

    @RequestMapping("/tostockmanage")
    public String tostockmanage() {
        return "product/stock/Stock";
    }

    @RequestMapping("/toproductstockmanage")
    public String toproductstockmanage() {
        return "product/stock/StockNumber";
    }

}
